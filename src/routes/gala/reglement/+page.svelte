<section class="reglements">
  <p class="filariane">
    <a href="/gala">Cérémonie des pépites</a> >
    <a href="#" class="active">Règlement</a>
  </p>

  <h2>Article 1 – <span class="gold">Objet</span></h2>
  <p>
    Le présent règlement définit les modalités d'organisation et de
    participation à l'événement "<span class="semibold italic"
      >La cérémonie des Pépites</span
    >", une cérémonie visant à récompenser les meilleurs projets réalisés par
    les étudiant.e.s en MMI durant l'année académique 2024-2025.<br />
    L'événement se déroulera le
    <span class="semibold">09 avril 2025, de 18h à 19h30</span>, au plateau TV
    de l'IUT Bordeaux Montaigne.Ce règlement précise les critères de
    participation, de sélection et d'attribution des prix.
  </p>
  <h2>
    Article 2 – <span class="gold">Conditions de participation</span>
  </h2>
  <p>
    <span class="semibold">Éligibilité</span><br />
    Respect des critères de sélection (cf.
    <span class="semibold">Article 4 - Catégories et critères de sélection</span
    >)<br /><br />
    <span class="semibold">Dépôt des candidatures</span><br />
    &nbsp; • Les participant.e.s doivent soumettre leur projet
    <span class="semibold">via un dépôt sur un drive</span>
    dédié, selon les modalités communiquées par l'organisation.<br />
    &nbsp; • Les projets seront
    <span class="semibold">examinés par le Bureau des Étudiants (BDE)</span> afin
    de vérifier leur conformité avec le présent règlement.
  </p>
  <h2>
    Article 3 – <span class="gold">Processus de sélection et votes</span>
  </h2>
  <p>
    <span class="semibold">Phase de sélection</span><br />
    &nbsp; • Après vérification des dossiers, une présélection sera effectuée par
    le jury afin de
    <span class="semibold">retenir 4 nominé.e.s par catégorie</span>.<br />
    <br /> <span class="semibold">Vote</span><br />
    Le vote final repose sur :<br />
    &nbsp; • <span class="semibold">Une note attribuée par le jury</span>,
    composé d'anciens élèves MMI et d'enseignants.<br />
    &nbsp; • Pour le “prix du public“ uniquement,
    <span class="semibold">une note attribuée par le public</span>
    via un système de vote en ligne.<br />
    Les lauréat.e.s seront annoncé.e.s lors de La cérémonie officielle.
  </p>
  <h2>
    Article 4 – <span class="gold">Catégories et critères de sélection</span>
  </h2>
  <p>
    <span class="semibold">Critères généraux</span><br />
    &nbsp; • Le projet doit avoir été réalisé en
    <span class="semibold">MMI durant l’année 2024-2025</span>
    (sauf pour la catégorie "Meilleur projet personnel").<br />
    &nbsp; • Il doit être <span class="semibold">consultable</span> (en ligne ou
    accessible sous une forme adaptée).<br />
    &nbsp; • Travail d’équipe : Si réalisé en groupe,
    <span class="semibold">mentionner clairement les contributions</span>
    de chacun.<br />
    &nbsp; • Présentation : Un
    <span class="semibold">court texte de description du projet</span>
    est exigé (objectifs, inspirations, outils utilisés).<br />
    <br />
    <span class="semibold">Catégories et critères spécifiques</span>
    <br />1. Meilleur Site Web<br />
    &nbsp; • Le site doit être
    <span class="semibold">hébergé et accessible</span>
    en ligne.<br />
    &nbsp; • Tous les <span class="semibold">liens</span> doivent être
    <span class="semibold">fonctionnels</span>.<br />
    &nbsp; • Le site doit être
    <span class="semibold">complet et terminé</span>.<br />
    &nbsp; • Être adapté aux écrans mobiles et tablettes. (<span
      class="semibold">Responsive</span
    >)<br />
    &nbsp; • Respecter les <span class="semibold">bonnes pratiques</span>
    (contrastes, alternatives textuelles, navigation intuitive).<br />
    &nbsp; • <span class="semibold">Pas de chargement excessif</span>,
    optimisation des médias.<br />
    <br />2. Meilleure Maquette<br />
    &nbsp; • La maquette doit être <span class="semibold">fonctionnelle</span>
    (navigable sous forme de prototype interactif).<br />
    &nbsp; • Respecter une
    <span class="semibold">identité graphique homogène</span>.<br />
    <br />3. Meilleur Court Métrage<br />
    &nbsp; • Disponible <span class="semibold">en ligne</span>.<br />
    &nbsp; • Présence de <span class="semibold">crédits de fin</span>.<br />
    &nbsp; • Utiliser des <span class="semibold">assets libres de droits</span>
    uniquement.<br />
    &nbsp; • <span class="semibold">Son normalisé</span> : niveau cible -1dbtp,
    target loudness -14lkfs pour une écoute homogène.<br />
    &nbsp; • <span class="semibold">Sous-titres en français</span>
    obligatoires.<br />
    &nbsp; • Présence d’un <span class="semibold">titre</span>.<br />
    &nbsp; • <span class="semibold">Image stable et bien cadrée</span>.<br />
    <br /> 4. Meilleur Projet Personnel<br />
    &nbsp; • Se référer aux
    <span class="semibold">critères de la catégorie correspondante</span>.<br />
    <br /> 5. Meilleur Portfolio<br />
    &nbsp; • Doit être <span class="semibold">en ligne ou accessible</span> sous
    un format adapté.<br />
    &nbsp; • Doit contenir un minimum de réalisations démontrant les compétences
    du candidat.<br />
    <br />6. Meilleur Gâteau<br />
    &nbsp; • Obligatoirement <span class="semibold">comestible</span>.<br />
    <br /> 7. Meilleur Making Of<br />
    &nbsp; • Doit provoquer au moins
    <span class="semibold">un rire du jury</span>.<br />
    &nbsp; • Mettre en avant des
    <span class="semibold">instants marquants de la création</span>.<br />
    <br />8. Meilleure Affiche<br />
    &nbsp; • Présence d’un <span class="semibold">titre</span>.<br />
    &nbsp; • Contenir les
    <span class="semibold">noms des participants</span>.<br />
    &nbsp; • <span class="semibold">Format A3</span>.
  </p>
  <h2>
    Article 5 – <span class="gold">Droits à l'image et diffusion</span>
  </h2>
  <p>
    &nbsp; • Les participants <span class="semibold">acceptent</span> que leurs
    projets et images puissent être utilisés à des fins promotionnelles par le
    BDE et l'IUT.<br />
    &nbsp; • Toute participation
    <span class="semibold">implique l'autorisation de diffusion</span> sur les réseaux
    sociaux et plateformes officielles de l'événement.
  </p>
  <h2>Article 6 – <span class="gold">disqualifications</span></h2>
  <p>
    &nbsp; • Toute tentative de tricherie ou de non-respect des critères
    entraînera une
    <span class="semibold">disqualification immédiate</span>.
  </p>
  <h2>Article 7 – <span class="gold">Acceptation du règlement</span></h2>
  <p>
    La participation à "<span class="semibold italic"
      >La cérémonie des Pépites</span
    >" implique l'acceptation pleine et entière du présent règlement par
    l'ensemble des candidat.e.s.
  </p>
  <p class="end-reglement">
    <span class="semibold">Règlement écrit le 21/02/2025</span>
  </p>
